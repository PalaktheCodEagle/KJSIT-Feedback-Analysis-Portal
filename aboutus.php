            <h3>Department of Computer Engineering</h3>
            <br>
            <p>The Computer Engineering department was established in the year 2001 to impart quality education. The
                department has well qualified and motivated faculty members and support staff. The laboratories are
                adequately
                equipped with state-of-the-art facilities. The students are members of various professional bodies like
                IET,
                CSI, IEEE, NSS etc. Various platforms are available for students, like project competition, technical &
                cultural
                festivals, international conference, etc. to showcase their talent. It is a regular practice of the
                department
                to organize industrial visits, expert talks, workshops and internship in addition to the latest
                certification
                courses for students in the field of Computer engineering. Student have won prizes in various national
                and
                international level paper presentation, competitions, project exhibition etc. As the department has good
                industry interaction and alumni support, students get several opportunities of internship, project
                guidance,
                placement and many more.
                <br>
                <br>
                The department is accredited by NBA in 2018 and intake has doubled to 120 from academic year 2019-20.
                Besides
                offering undergraduate (B. Tech. in Computer Engineering) it also offers Post Graduation (M.Tech.) in
                Artificial
                Intelligence. As the autonomous status is awarded by UGC from academic year 2021-22, curricula have
                revised for
                UG and PG programs by Board of Studies. Exposure courses like Skill based, Activity based and Technology
                based
                courses are added to motivate students to participate in various activities. Under Project Based
                Learning (PBL)
                mini, minor and major projects are introduced from sem III to Sem VIII which help students to work in a
                team and
                develop projects using latest technologies. The department has to its credits of maximum number of
                placements of
                the students in Infosys, TCS, Accenture, Cognizant, L&T Infotech, CSC, Tech Mahindra, Mastek, ICON,
                Majesco,
                MuSigma, BNP Paribas. Every year, few students opt for pursuing higher studies at prestigious
                universities in
                India and abroad.
